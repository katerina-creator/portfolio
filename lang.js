/**
 * Update the UI text based on selected language
 */
function setLang(lang) {
    if (!translations[lang]) return;
    localStorage.setItem('lang', lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (key.includes('_desc') && key.includes('job')) {
                el.innerHTML = translations[lang][key].replace(/\n/g, '<br>');
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    document.documentElement.lang = lang;
}

/**
 * Handle Avatar Fallback
 */
function initAvatar() {
    const img = document.querySelector('.avatar-img');
    const fallback = document.querySelector('.avatar-fallback');

    if (img && fallback) {
        // If image fails to load (or is missing)
        img.addEventListener('error', () => {
            img.style.display = 'none';
            // Fallback is situated behind via CSS z-index, so hiding img reveals it
        });
    }
}

/**
 * Initialize Event Listeners
 */
function init() {
    // Language Buttons
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            setLang(lang);
        });
    });

    // Accordions
    const accordions = document.querySelectorAll('.accordion-header');
    accordions.forEach(header => {
        header.addEventListener('click', () => {
            const isExpanded = header.getAttribute('aria-expanded') === 'true';
            header.setAttribute('aria-expanded', !isExpanded);
            const panel = header.nextElementSibling;
            if (panel) {
                if (isExpanded) {
                    panel.setAttribute('hidden', '');
                } else {
                    panel.removeAttribute('hidden');
                }
            }
        });
    });

    // Avatar
    initAvatar();

    // Load saved settings
    const savedLang = localStorage.getItem('lang') || 'en';
    setLang(savedLang);
}

// Init
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
} else {
    init();
}
